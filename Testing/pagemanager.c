int options(char (*given_options)[20],int given)// take an array of options...each option can have a max of 25 characters including null
{


    int op;
take_options:
    // system("cls");//super sluggish...takes 5 seconds to clear each time

    printf("\e[1;1H\e[2J");     //instantaneous but does not actually clear screen
    title();
    for(int i=0;i<given;i++)
    {
        printf("%s\n",*(given_options+i));

    }
    printf("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~\n");
    printf("\n\n>");
    op=getch()-'0';
    if(op>0 &&  op<=given)
    {
        printf("\e[1;1H\e[2J");
        title();
        //printf("\e[1;1H\e[2J");
        printf("%s",given_options[op-1]);
        sleep(1);
        printf("\e[1;1H\e[2J");
        title();
        return op;
    }
    else
        {
            printf("Invalid choice\n");
            printf("re-enter options...");
            sleep(1);
            printf("...\n");
            goto take_options;  //only op being assigned so works fine
        }

}



